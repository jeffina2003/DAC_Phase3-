import pandas as pd
import matplotlib.pyplot as plt

# Step 1: Load the user-provided dataset
dataset_path = input("Enter the path to the water_potability.csv file: ")
try:
    df = pd.read_csv(dataset_path)
except FileNotFoundError:
    print("File not found. Make sure you provide the correct file path.")
    exit(1)

# Step 2: Data Exploration and Analysis
# Summary statistics
summary_stats = df.describe()

# Check for missing values
missing_values = df.isnull().sum()

# Count of potable and non-potable water
potability_counts = df['Potability'].value_counts()

# Step 3: Data Visualization
plt.figure(figsize=(12, 6))
plt.bar(potability_counts.index, potability_counts.values)
plt.title("Distribution of Potable vs. Non-Potable Water")
plt.xlabel("Potability")
plt.ylabel("Count")
plt.xticks([0, 1], ["Non-Potable", "Potable"])
plt.show()

# Step 4: Display results
print("Summary Statistics:")
print(summary_stats)
print("\nMissing Values:")
print(missing_values)
print("\nDistribution of Potable vs. Non-Potable Water:")
print(potability_counts)

